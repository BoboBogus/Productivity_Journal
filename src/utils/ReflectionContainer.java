package utils;

public class ReflectionContainer {
    String Date;
    String Goal;
    String Description;
    int stars;
    int points;

    public ReflectionContainer(String Date, String Goal, String Description, int stars, int points) {
        this.Date = Date;
        this.Goal = Goal;
        this.Description = Description;
        this.stars = stars;
        this.points = points;
    }
}
